export function getJournalByTopic(topic: string) {
  const keywords = topic.toLowerCase().split(' ');
  return journalDatabase.filter(journal => 
    keywords.some(keyword => 
      journal.title.toLowerCase().includes(keyword) ||
      journal.topic.toLowerCase().includes(keyword) ||
      journal.summary.toLowerCase().includes(keyword)
    )
  );
}

export function getAllJournals() {
  return journalDatabase;
}

export function getJournalById(id: string) {
  return journalDatabase.find(j => j.id === id);
}
